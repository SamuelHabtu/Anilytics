DROP TABLE anime;
CREATE TABLE anime(
  anime_id INT PRIMARY KEY,
  name VARCHAR(200) NULL,
  score DECIMAL(4,2) NULL,
  genres VARCHAR(200) NULL,
  english_name VARCHAR(200) NULL,
  japanese_name VARCHAR(200) NULL,
  type VARCHAR(100) NULL,
  episodes INT NULL,
  aired VARCHAR(100) NULL,
  premiered VARCHAR(20) NULL,
  producers VARCHAR(500) NULL,
  licensors VARCHAR(200) NULL,
  studios VARCHAR(200) NULL,
  source VARCHAR(100) NULL,
  duration VARCHAR(100) NULL,
  rating VARCHAR(100) NULL,
  ranked DECIMAL(6,1) NULL,
  popularity INT NULL,
  members INT NULL,
  favorites INT NULL,
  watching INT NULL,
  completed INT NULL,
  on_hold INT NULL,
  dropped INT NULL,
  plan_to_watch INT NULL,
  score_10 DECIMAL(10,1) NULL,
  score_9 DECIMAL(10,1) NULL,
  score_8 DECIMAL(10,1) NULL,
  score_7 DECIMAL(10,1) NULL,
  score_6 DECIMAL(10,1) NULL,
  score_5 DECIMAL(10,1) NULL,
  score_4 DECIMAL(10,1) NULL,
  score_3 DECIMAL(10,1) NULL,
  score_2 DECIMAL(10,1) NULL,
  score_1 DECIMAL(10,1) NULL
);